// wap to retrive array element using for while loop

class arrayRetriveUsingWhileLoop
{
    public static void main(String[] args)
     {
        int[] a={12,11,23,123};

        int i=0;
        while(i<a.length)
         {
          System.out.print(a[i]+" ");
          i++;
          }

      }
    
    
}
