// wap to retrive array element using for Do while loop

class arrayRetriveUsingDoWhileLoop
{
    public static void main(String[] args)
     {
        int[] a={12,11,23,123};

        int i=0;
        do
         {
          System.out.print(a[i]+" ");
          i++;
          }while(i<a.length);

      }
    
    
}